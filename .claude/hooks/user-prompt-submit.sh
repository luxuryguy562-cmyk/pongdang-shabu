#!/usr/bin/env bash
# UserPromptSubmit hook — 매 사장님 발화 후 자동 실행
# 1. 헌법 1-9 (사용설명서처럼) 자가 점검 박음
# 2. 빙산 트리거 키워드 자동 감지 → 헌법 1-8 메타 스캔 강제 발동

cat <<'HEREDOC1'
═══════════════════════════════════════════════
📋 헌법 1-9 자가 점검 (답변 작성 직전 5초)
═══════════════════════════════════════════════
- [ ] 영어 단어 등장 → 옆에 한국어 풀이 (사용설명서처럼)?
       예: "critic(검증 도구)", "main(본가)", "merge(합침)"
- [ ] 비유 안 썼나? (사장님 "비유로" 명시 X면 비유 금지)
- [ ] 첫 줄 = 결론 직접?
- [ ] 사장님 3초 안에 결론 파악?
- [ ] 결과 중심? ("사장님께 무슨 변화" O / "무엇을 했는가" X)
═══════════════════════════════════════════════
HEREDOC1

# 빙산 트리거 감지 (헌법 1-8 메타 스캔 강제 — CTO 마음먹기 X)
INPUT=$(cat 2>/dev/null || echo "")
PROMPT=""
if command -v jq >/dev/null 2>&1; then
  PROMPT=$(echo "$INPUT" | jq -r '.prompt // empty' 2>/dev/null)
fi
[ -z "$PROMPT" ] && PROMPT="$INPUT"

if echo "$PROMPT" | grep -qE "또|매번|맨날|왜 또|왜 자꾸|이상하다|이게 맞아|전에도|지난번|표면적|일일이|전체 흐름|고통|짜증"; then
  cat <<'HEREDOC2'

🚨 빙산 트리거 감지 — 헌법 1-8 메타 스캔 강제 발동
═══════════════════════════════════════════════
사장님 발화에 빙산 키워드 박힘 (또 / 매번 / 이상하다 / 전에도 / 표면적 / 고통 / 짜증 등).

CTO 즉시 의무 (답변 작성 전 강제):
1. 사장님 호소 카테고리 판정 (헌법 3-5 8개 중 어느 자리?)
2. grep -n "키워드" docs/dev_lessons.md docs/work_log.md (과거 호소 누적 확인)
3. 3건 이상 발견 시 → 즉시 빙산 보고 양식 박음:

   🔍 [메타 스캔] 빙산 의심
   - 카테고리: <명>
   - 과거 호소: dev_lessons #XX (총 N건)
   - 표면만 처리하면: <반복 예상>
   - 근본 해결안: <안>
   - 추천: <표면 / 근본>

→ 발견 시 기존 답변 폐기 + 메타 스캔 처음부터.
═══════════════════════════════════════════════
HEREDOC2
fi
